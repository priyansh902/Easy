class ParticleExample {
  final String japanese;   // full Japanese sentence
  final String reading;    // furigana / reading hint
  final String english;    // English translation
  const ParticleExample(this.japanese, this.reading, this.english);
}

class ParticleQuiz {
  final String question;     // sentence with ___ for the particle
  final String answer;       // the correct particle
  final String fullSentence; // complete Japanese sentence
  final String english;      // English meaning
  const ParticleQuiz(this.question, this.answer, this.fullSentence, this.english);
}

class Particle {

  final List<ParticleQuiz>   quizzes;    // 15 quiz questions
  const Particle({
    required this.symbol,
    required this.reading,
    required this.role,
    required this.explanation,
    required this.tip,
    required this.examples,
    required this.quizzes,
  });
}

